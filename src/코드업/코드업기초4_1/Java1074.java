package 코드업.코드업기초4_1;

import java.util.Scanner;

public class Java1074 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int i = scanner.nextInt();

        while (i != 0){
            System.out.println(i);
            i --;
        }
        scanner.close();
    }
}
